loop: 
  lw   x1, 0x200(x0)
  lw   x2, 0x204(x0)

  sw   x1, 0x210(x0)
  sw   x2, 0x214(x0)

  add  x4, x1, x2
  sw   x4, 0x218(x0)

  jal  x0, loop